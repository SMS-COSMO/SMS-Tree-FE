<template>
  <div class="top-margin middle">
    <el-alert title="第一次登录完毕后请修改密码！" show-icon type="warning" effect="dark" :closable="false" />
    <el-card style="margin-top: 15px">
      <template #header>
        登录
      </template>
      <el-form label-position="top">
        <el-form-item>
          <div class="icon-label">
            <el-icon :size="15">
              <User />
            </el-icon>
            用户名 / 学号
          </div>
          <el-input v-model="form.username" />
        </el-form-item>
        <el-form-item>
          <div class="icon-label">
            <el-icon :size="15">
              <Key />
            </el-icon>
            密码
          </div>
          <el-input type="password" v-model="form.password" show-password />
        </el-form-item>
        <el-form-item style="margin-bottom: 0;">
          <el-button class="submit-button" color="#146E3C" :loading="buttonLoading" @click="login()">
            登录
          </el-button>
        </el-form-item>
      </el-form>
    </el-card>
  </div>
</template>

<script lang="ts" setup>
import { reactive, ref } from 'vue';
import { trpc, isTRPCClientError } from '../../api/trpc';
import { ElMessage } from 'element-plus';

import { UserStore } from '../../stores/user';
const userStore = UserStore();

const buttonLoading = ref(false);

const form = reactive({
  username: '',
  password: ''
});

const login = () => {
  buttonLoading.value = true;
  trpc.user.login.mutate({ username: form.username, password: form.password })
    .then(res => {
      console.log(res);
      userStore.login({
        accessToken: res.accessToken,
        refreshToken: res.refreshToken,
        username: form.username
      });

      buttonLoading.value = false;
    })
    .catch(err => {
      if (isTRPCClientError(err)) {
        ElMessage({
          message: err.data?.code,
          type: 'error',
          showClose: true,
        });
      }

      buttonLoading.value = false;
    });
};
</script>

